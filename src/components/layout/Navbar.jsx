import React, { useRef, useState, useEffect } from "react";
import { LuChevronDown, LuMenu, LuPhoneCall } from "react-icons/lu";
import { Link, NavLink, useLocation } from "react-router-dom";
import useClickOutside from "@/hooks/useClickOutside";
import HyperLink from "../ui/HyperLink";
import LanguageToggle from "./LanguageToggle";
import { useTranslation } from "react-i18next";
import { useGetEquipmentsByDeptIdQuery } from "@/redux/api/equipmentsApi";

export default function Navbar() {
  const { data } = useGetEquipmentsByDeptIdQuery();
  const location = useLocation();
  const { t, i18n } = useTranslation();
  const [activeMenu, setActiveMenu] = useState(null);
  const [activeMobileMenu, setActiveMobileMenu] = useState(null);
  const [menuOpen, setMenuOpen] = useState(false);
  const [isSticky, setIsSticky] = useState(false);
  const menuRef = useRef(null);
  const sideBarRef = useRef(null);

  const toggleMobileSubmenu = (idx) =>
    activeMobileMenu === idx
      ? setActiveMobileMenu(null)
      : setActiveMobileMenu(idx);

  useClickOutside(menuRef, () => setActiveMenu(null));
  useClickOutside(sideBarRef, () => setMenuOpen(false));

  const currentLang = i18n.language === "ar" ? "ar" : "en";

  const nav = [
    {
      title: t("nav.home"),
      href: "/",
    },
    {
      title: t("nav.about.title"),
      submenu: [
        {
          title: t("nav.about.submenu.corporateProfile"),
          href: "/about",
        },
        {
          title: t("about.visionMissionValues"),
          href: "/about/vision-mission-values",
        },
        {
          title: t("nav.about.submenu.chairmanMsg"),
          href: "/about/chairman-msg",
        },
        {
          title: t("nav.about.submenu.mdMsg"),
          href: "/about/md-msg",
        },
        {
          title: t("nav.about.submenu.executiveManagement"),
          href: "/about/executive-management",
        },
        {
          title: t("nav.about.submenu.qualifications"),
          href: "/about/qualifications",
        },
        {
          title: t("nav.about.submenu.subsidiaries"),
          href: "/about/subsidiary",
        },
      ],
    },
    {
      title: t("nav.services.title"),
      href: "/services",
      submenu: data?.data?.map((d) => ({
        title: d?.name[currentLang],
        href: `/services/service/${d?.href}/${d._id}`,
      })),
    },
    {
      title: t("nav.qhse.title"),
      href: "/qhse",
    },
    {
      title: t("nav.projects.title"),
      href: `/projects`,
    },
    {
      title: t("nav.news.title"),
      submenu: [
        {
          title: t("nav.news.submenu.newsEvents"),
          href: "/news",
        },
        {
          title: t("nav.news.submenu.csr"),
          href: "/news/csr",
        },
        {
          title: t("nav.news.submenu.photoGallery"),
          href: "/news/photo-gallery",
        },
        {
          title: t("nav.news.submenu.videoGallery"),
          href: "/news/video-gallery",
        },
        {
          title: t("nav.news.submenu.publication"),
          href: "/news/publication",
        },
      ],
    },
    // {
    //   title: t("nav.careers"),
    //   href: "/careers",
    // },
    {
      title: t("nav.facilities"),
      href: "/facilities",
    },
  ];

  const handleScroll = () =>
    window.scrollY > 200 ? setIsSticky(true) : setIsSticky(false);

  useEffect(() => {
    window.addEventListener("scroll", handleScroll);
    return () => {
      window.removeEventListener("scroll", handleScroll);
    };
  }, []);

  const isTransparent =
    location.pathname === "/" || location.pathname === "/services";

  return (
    <header
      className={`z-50 w-full p-3 lg:p-5 ${!isTransparent ? "bg-blue/90" : "absolute top-0 bg-transparent"
        }`}
    >
      <div
        className={
          isSticky && "fixed left-0 top-0 z-50 mx-auto w-full bg-blue/90"
        }
      >
        <div
          className={
            isSticky
              ? "right-0 z-50 mx-auto hidden w-full max-w-[1280px] gap-5 p-3 transition-transform duration-300 ease-in-out lg:flex lg:py-5"
              : "mx-auto max-w-[1280px] space-y-5"
          }
        >
          <div className="flex items-center justify-between">
            <a
              href={import.meta.env.VITE_HOME_URL}
              target="_blank"
              rel="noopener noreferrer"
              aria-label="logo"
              className="shrink-0"
            >
              <img
                src="/logo.png"
                alt="Logo"
                className="h-auto w-[180px] object-contain"
              />
            </a>

            {!isSticky && (
              <div className="relative hidden items-center justify-between rounded-md bg-transparent px-3 py-1.5 text-textGray lg:flex">
                <LanguageToggle />
              </div>
            )}

            <button
              onClick={() => setMenuOpen(!menuOpen)}
              className="lg:hidden"
            >
              {<LuMenu size={20} className="text-white" />}
            </button>
          </div>

          <DesktopMenu
            nav={nav}
            activeMenu={activeMenu}
            setActiveMenu={setActiveMenu}
            isTransparent={isTransparent}
          />
        </div>
      </div>

      {/* mobile menu */}
      <nav
        className={`fixed top-0 z-50 h-screen overflow-y-auto overflow-x-hidden py-5 duration-700 lg:hidden ${menuOpen ? "right-0" : "-right-full"
          } w-full max-w-[300px] bg-black sm:max-w-[350px]`}
        ref={sideBarRef}
      >
        <div className="flex w-full flex-col justify-between gap-5 py-5 text-sm">
          {nav.map((nav, idx) => (
            <div key={idx} className="font-kanit group relative">
              {!nav.submenu ? (
                <NavLink
                  to={nav?.href}
                  onClick={() => {
                    setMenuOpen(false);
                  }}
                  className="flex items-center py-2 pl-10 text-sm text-textGray rtl:pr-10"
                >
                  {nav?.title}
                </NavLink>
              ) : (
                <button
                  onClick={() => nav.submenu && toggleMobileSubmenu(idx)}
                  className="flex w-full items-center py-2 pl-10 text-sm text-textGray rtl:pr-10"
                >
                  {nav?.title}
                  <LuChevronDown
                    strokeWidth={2.5}
                    className={`${activeMenu === idx && "rotate-180"
                      } ml-1.5 transform transition-transform rtl:mr-1.5`}
                  />
                </button>
              )}

              {nav.submenu && activeMobileMenu === idx && (
                <div className="top-full mt-2 w-[20rem] overflow-hidden rounded-lg text-textGray shadow-sm">
                  {nav.submenu.map((subnav, subIdx) => (
                    <NavLink
                      key={subIdx}
                      to={subnav?.href}
                      onClick={() => {
                        setMenuOpen(false);
                      }}
                      className="block text-wrap break-words py-3 pl-12 rtl:pl-0 rtl:pr-12"
                    >
                      {subnav?.title}
                    </NavLink>
                  ))}
                </div>
              )}
            </div>
          ))}
          <Link
            to="/contact"
            onClick={() => setMenuOpen(false)}
            className="tex-sm ml-8 flex w-fit items-center justify-between gap-x-3 rounded-md border border-red px-3 py-1 font-normal text-red rtl:ml-0 rtl:mr-8"
          >
            <span>{t("nav.contact_us")}</span>
            <span>
              <LuPhoneCall />
            </span>
          </Link>

          <div className="ml-8 mt-3 rtl:mr-8">
            <LanguageToggle />
          </div>
        </div>
      </nav>
    </header>
  );
}

function DesktopMenu({ nav, activeMenu, setActiveMenu, isTransparent }) {
  const { t } = useTranslation();
  return (
    <nav className="font-kanit hidden w-full items-center gap-1 px-3 lg:flex">
      <div className="flex w-full items-center justify-between">
        {nav.map((nav, idx) => (
          <div
            key={idx}
            className="relative z-50"
            onMouseEnter={() => setActiveMenu(idx)}
            onMouseLeave={() => setActiveMenu(null)}
          >
            {!nav.submenu ? (
              <NavLink
                to={nav?.href}
                className="flex items-center text-[0.9rem] text-textGray"
              >
                {nav?.title}
              </NavLink>
            ) : (
              <button
                onClick={() => nav.submenu && toggleSubmenu(idx)}
                className="flex items-center text-[0.9rem] text-textGray"
              >
                {nav?.title}

                <LuChevronDown
                  strokeWidth={2.5}
                  className={`${activeMenu === idx && "rotate-180"
                    } ml-1.5 transform transition-transform rtl:mr-1.5`}
                />
              </button>
            )}
            {nav.submenu && activeMenu === idx && (
              <div className="absolute left-0 top-full w-[20rem] overflow-hidden pt-4 rtl:right-0">
                <div className="rounded-lg bg-white shadow-sm">
                  {nav.submenu.map((subnav, subIdx) => (
                    <NavLink
                      key={subIdx}
                      to={subnav?.href}
                      className="block border-b border-gray-300 px-5 py-2.5 text-[0.9rem] text-blue hover:border-l-[4px] hover:border-l-red rtl:hover:border-l-0 rtl:hover:border-r-[4px] rtl:hover:border-r-red"
                    >
                      {subnav?.title}
                    </NavLink>
                  ))}
                </div>
              </div>
            )}
          </div>
        ))}
        <HyperLink
          href="/contact"
          name="contact us"
          className="border-[#F2F3F5] bg-[#F2F3F5]/[0.12] text-sm text-[#F2F3F5]"
          children={t("nav.contact_us")}
          variant="outline"
          icon={<LuPhoneCall />}
        />
      </div>
    </nav>
  );
}
